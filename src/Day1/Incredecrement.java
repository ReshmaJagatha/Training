package Day1;

public class Incredecrement {
public static void main(String[] args) {
	int a=75;
	++a;
	System.out.println(a);//76
	int b=57;
	b++;
	System.out.println(b);//58
	int c=38;
	c--;
	System.out.println(c);//37
	int d=38;
	--d;
	System.out.println(d);//37
	int h=11;
	int res=h++;
	System.out.println(res);//11
	System.out.println(h);//12
	int j=70;
	int r=j--;
	System.out.println(r);//70
	System.out.println(j);//69
	int k=10;

	int m=k++;
	System.out.println(k);//11
	System.out.println(m);//10
}
}
