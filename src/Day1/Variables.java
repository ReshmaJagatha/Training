package Day1;

public class Variables {
 public static void main(String[] args) {
 int a=10;
 a=3000;
 System.out.println(a);//3000
 int b=500;
 System.out.println(b);//500
 System.out.println(a+b);//3500
 int c=a+b;
 System.out.println(c);//3500
 
 String s="Reshma";
 System.out.println(s);
 
 byte by=50;
 System.out.println(by);
 
 long l=345678l;
 System.out.println(l);
 float f=45.89f;
 System.out.println(f);
 
 System.out.println(l+f);
 boolean bo=true;
 System.out.println(bo);//true
 char ch='A';
 System.out.println(ch);//A
}
}
